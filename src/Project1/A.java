package Project1;

public class A {
    private int a;
    private int b;

    public int getA() {
        return a;
    }

    public int getB() {
        return b;
    }

    public void setAB(int num1, int num2) {
        a = num1;
        b = num2;
    }

    public int getSum() {
        return a + b;
    }

    public int getProduct() {
        return a * b;
    }
}
